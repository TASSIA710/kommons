package net.tassia

/**
 * The second-to-millis multiplier.
 *
 * @since Kommons 1.0
 * @author Tassilo
 */
const val SECOND = 1000L

/**
 * The minute-to-millis multiplier.
 *
 * @since Kommons 1.0
 * @author Tassilo
 */
const val MINUTE = SECOND * 60

/**
 * The hour-to-millis multiplier.
 *
 * @since Kommons 1.0
 * @author Tassilo
 */
const val HOUR = MINUTE * 60

/**
 * The day-to-millis multiplier.
 *
 * @since Kommons 1.0
 * @author Tassilo
 */
const val DAY = HOUR * 24

/**
 * The week-to-millis multiplier.
 *
 * @since Kommons 1.0
 * @author Tassilo
 */
const val WEEK = DAY * 7

/**
 * The month-to-millis multiplier.
 *
 * @since Kommons 1.0
 * @author Tassilo
 */
const val MONTH = DAY * 30

/**
 * The year-to-millis multiplier.
 *
 * @since Kommons 1.0
 * @author Tassilo
 */
const val YEAR = DAY * 365
